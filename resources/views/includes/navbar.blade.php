<script>
    function getDaysTogether() {
        var startDate = new Date(2022, 2, 19);

        setInterval(function() {
            var currentDate = new Date();
            var diff = Math.abs(currentDate - startDate);

            var days = Math.floor(diff / (1000 * 60 * 60 * 24));
            var hours = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            var minutes = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
            var seconds = Math.floor((diff % (1000 * 60)) / 1000);

            var daysTogether = days + " days, " + hours + " hours, " + minutes + " minutes, " + seconds +
                " seconds";

            document.getElementById("daysTogether").innerHTML = "Time together: " + daysTogether;
        }, 1000);
    }

    getDaysTogether();
</script>
